    class Program
    {
        static void Main(string[] args)
        {
            StreamWriter sw;
            string language = string.Empty;
            int count = Enum.GetNames(typeof(Languages)).Length;
            for (int i = 0; i < count; i++)
            {
                language = Enum.GetValues(typeof(Languages)).GetValue(i).ToString();
                Console.WriteLine(language);
                Directory.CreateDirectory(@"C:\temp\Languages");
                Directory.CreateDirectory("C:\\temp\\Languages\\" + language );
                sw = new StreamWriter("C:\\temp\\Languages\\" + language  +"\\log.txt");
                sw.WriteLine("File #" + i + " created on " + DateTime.Now);
                sw.Close();
            }
        }
    }

